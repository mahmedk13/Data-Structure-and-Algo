package com.org.stack;

public class Person {
	
	private String name;
	private int salary;
	
	public Person(String name, int salary) {
		super();
		this.name = name;
		this.salary = salary;
	}

	@Override
	public String toString() {
		return "Person [name=" + name + ", salary=" + salary + "]";
	}
	
	
	
	
	

}
